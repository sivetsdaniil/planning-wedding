def apply_replacements(text: str, rules: list[tuple[str, str]]) -> str:
    for old, new in rules:
        if old in text:
            text = text.replace(old, new)
    return text


def set_paragraph_text(paragraph, text: str) -> None:
    if paragraph.text == text:
        return
    for run in paragraph.runs:
        run.text = ""
    if paragraph.runs:
        paragraph.runs[0].text = text
    else:
        paragraph.add_run(text)


def cleanup_leftovers(doc: Document) -> int:
    """Финальный проход: убрать остатки старой темы."""
    patterns = [
        (re.compile(r"трехмерн\w*\s+чат\w*", re.I), "веб-приложение «Eternal Moments»"),
        (re.compile(r"\bUnreal\b[^.;\n]*", re.I), "Express"),
        (re.compile(r"мультиплеер\w*", re.I), "клиент-серверное взаимодействие"),
        (re.compile(r"blueprint", re.I), "маршрут"),
    ]
    changes = 0
    for p in doc.paragraphs:
        t = p.text
        if not t.strip():
            continue
        new = t
        for rx, repl in patterns:
            new = rx.sub(repl, new)
        new = re.sub(r"\s{2,}", " ", new).strip()
        if new != t:
            set_paragraph_text(p, new)
            changes += 1
    return changes


def process_document(doc: Document) -> int:
    changes = 0
    rules = GLOBAL_REPLACEMENTS

    for i, p in enumerate(doc.paragraphs):
        if i in PARA_OVERRIDES:
            new = PARA_OVERRIDES[i]
            if p.text != new:
                set_paragraph_text(p, new)
                changes += 1
            continue

        if i in CODE_PARA_RANGE:
            continue

        original = p.text
        if not original.strip():
            continue

        new = apply_replacements(original, rules)
        if new != original:
            set_paragraph_text(p, new)
            changes += 1

    # Приложение: заменить блок C++/Blueprint
    code_idx = 0
    for i in CODE_PARA_RANGE:
        if i >= len(doc.paragraphs):
            break
        p = doc.paragraphs[i]
        if not p.text.strip():
            continue
        if code_idx < len(APPENDIX_SAMPLE):
            set_paragraph_text(p, APPENDIX_SAMPLE[code_idx])
            code_idx += 1
            changes += 1
        elif any(
            k in p.text
            for k in ("UE_", "UGameplay", "AActor", "Blueprint", "#include", "void ")
        ):
            set_paragraph_text(p, "")
            changes += 1

    for table in doc.tables:
        for row in table.rows:
            for cell in row.cells:
                for p in cell.paragraphs:
                    orig = p.text
                    new = apply_replacements(orig, TABLE_REPLACEMENTS)
                    new = apply_replacements(new, rules)
                    if "Unreal" in new or "трехмерный чат" in new:
                        new = new.replace(
                            "Unreal Engine 5",
                            "Node.js, Express, PostgreSQL",
                        ).replace("Unreal", "")
                    if new != orig:
                        set_paragraph_text(p, new)
                        changes += 1

    return changes


def main() -> None:
    shutil.copy2(SRC, DST)
    doc = Document(str(DST))
    n = process_document(doc)
    n += cleanup_leftovers(doc)
    doc.save(str(DST))
    print(f"Source: {SRC}")
    print(f"Output: {DST}")
    print(f"Paragraph/table changes: {n}")


if __name__ == "__main__":
    main()
